public class Switch_2 {
    public static void main(String[] args){
        char a='E';
        switch (a){
            case 'A':
                System.out.println(a);
                break;
            case 'B':
                System.out.println(a);
                break;
            case 'C':
                System.out.println(a);
                break;
            case 'D': 
                System.out.println(a);
                break;
            default:
                System.out.println("not found");         
        }
    }
    
}
