package Loops.For;

public class For_1 {
    public static void main(String[] args){

        int n=10;
        System.out.println("to print "+n+" numbers continuosly");

        /**
         * for loop structure
         * for(intialization;condition;incrementation or decrementation){
         *   write for loop code here
         * }
         */
        for(int i=0;i<n;i++)
        {
            System.out.println(i);
        }
    }
    
}
